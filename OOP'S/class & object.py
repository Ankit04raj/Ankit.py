# CLASS & OBJECT :- class is a blueprint for creating objects.
# ___________________________________________________________________

# CREATING CLASS:-
class Student:    # the define class first letter("Student") is in "capital"
    name = "ankit raj"

# CREATING OBJECT(instance)
s1 = Student()
print(s1)   
print(s1.name)

s2 = Student()
print(s2.name)

# ANOTHER EXAMPLE:=
#firstly i creatd a class of car
class Car:
    color = "orange"
    brand = "royals royal"

#now create an object for class which is also known as (instance)
car1 = Car()
print(car1)
print(car1.color)
print(car1.brand)